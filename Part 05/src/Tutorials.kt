fun main(){
    val x = 15
    val y = 7
    val result = (x + y)

    println(result)

    val newResult = (19 + 6)
    println(newResult)

    println(25 + 12)
    println(15 / 7)

    var p = 20
    var q = 8
    println("The result of p + q is: ${p + q}")

    p += 10
    q *= 3
    println("The result of p + q is now: ${p + q}")

}
