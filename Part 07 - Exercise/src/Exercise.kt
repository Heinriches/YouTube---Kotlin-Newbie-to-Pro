// STRINGS - Part 7

//  For this exercise, you are to show your name first in uppercase,
//  and then in reverse order.

fun main(){
    val name = "Fabio"

    println("My name is: $name.")
    println("01 - In all uppercase-letters, my name is: ${name.uppercase()}.")
    println("02 - in reverse-order, my name is: ${name.uppercase().reversed()}.")
}